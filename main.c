/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Config code for STM32_WPAN (HSE Tuning must be done before system clock configuration) */
  MX_APPE_Config();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* IPCC initialisation */
  MX_IPCC_Init();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_I2C3_Init();
  MX_RTC_Init();
  MX_USART1_UART_Init();
  MX_RF_Init();
  /* USER CODE BEGIN 2 */

  IMUInit(); // initialize the IMU
  MagInit(); // initialize the magnetic sensor

  if ( (IMUState == IMU_IDLE) && (MagState == MAG_IDLE) )
  {
	  // toggle led to show initialisation success
	  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, SET);
	  HAL_Delay(500);
	  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, RESET);
  }
  /* USER CODE END 2 */

  /* Init code for STM32_WPAN */
  MX_APPE_Init();

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    MX_APPE_Process();

    /* USER CODE BEGIN 3 */
	  // magnetic sensor
	  if (MagSMFlag == 1)
	  {
	      MagSingleMode();
	  }
	  if ((MagState == MAG_SINGLEMEASUREMENT) && (MagDMABusy == 0)  && (MagSMFlag == 0))
	  {
		  MagRead();
	  }
	  if (MagGetDataFlag == 1)
	  {
	      MagGetData();
	  }

	  // IMU
	  if( (IMUDataReadyFlag == 1) && (IMUGetDataFlag == 0) && (IMUState == IMU_IDLE))
	  {
		  IMURead();
	  }
	  if (IMUGetDataFlag == 1)
	  {
		  IMUGetData();
	  }

	  // madgwick filter
	  if ( (IMUMadgwick==1) && (MagMadgwick == 1) )
	  {
		  IMUAverage();
		  MagAverage();
//		  UpdateMadgwick();		// put data through madgwick filter
		  MahonyAHRSupdate();

		  // update the sensor data for BLE transmission
		  P2PS_APP_Update_SensorData();

		  // reset the flags
		  IMUMadgwick = 0;
		  MagMadgwick = 0;
	  }

	  // UART
	  if ( UARTSendData == 1 )
	  {
		  SendDataOverUART();
		 // UARTSendData = 0;
	  }
	  if ( ClearSampleIndex == 1 )
	  {
		  ClearDataOverUART();
		 // ClearSampleIndex = 0;
	  }
  }
  /* USER CODE END 3 */
}

