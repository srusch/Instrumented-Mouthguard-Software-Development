// DMA Rx complete callback
void HAL_I2C_MemRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
	if (hi2c->Instance == I2C1)	// IMU
	{
		IMUDMABusy = 0; 	// reset IMU DMA flag

		if (IMUState == IMU_READING)	// if Rx complete in reading state
		{
			IMUState = IMU_COMPLETE;	// set state to be complete -> data reading is complete
			IMUGetDataFlag = 1;			// set the IMU get data flag
		}
	}

	if (hi2c->Instance == I2C3) // magnetic sensor
	{
        MagDMABusy = 0;  // Reset DMA busy flag

        if (MagState == MAG_READING)
        {
            MagState = MAG_COMPLETE;    // set state to be complete -> data reading is complete
            MagGetDataFlag = 1;         // set the magnetic sensor get data flag
        }
	}
}

